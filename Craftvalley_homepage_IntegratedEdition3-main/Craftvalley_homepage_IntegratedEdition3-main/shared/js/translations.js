// Helper: get translation by dot-notation key
function t(key, lang) {
  lang = lang || window.CV_LANG || "en";
  const keys = key.split(".");
  let val = T[lang];
  for (const k of keys) {
    if (val == null) return key;
    val = val[k];
  }
  return val != null ? val : key;
}
